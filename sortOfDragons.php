    <h2>10 types de dragons</h2>
    <ol>
        <li id="chinois" class="photos drag1">Le dragon chinois:</li>
        <p>
            Lune des légendes les plus populaires sur l'origine du dragon chinois est la théorie du culte totémique qui
            stipule que les dragons sont apparus parce que lempereur jaune Huangdi était parti en guerre contre neuf
            autres tribus.
            <br>
            Après avoir vaincu chaque tribu, il ajoutait leurs totems au sien. Au fil des victoires, son totem a grandi
            et est devenu un hybride de nombreux animaux, avec des yeux comme une crevette, des bois comme un cerf, une
            grande bouche comme un taureau, un nez comme un chien, des moustaches comme un poisson-chat, une crinière de
            lion, une longue queue comme un serpent, des écailles comme un poisson et des griffes comme un faucon. Ainsi
            naquirent les dragons chinois.
        </p>

        <li id="occidental" class="photos drag2">Le dragon occidental:</li>
        <p>
            Le dragon occidental est probablement celui qui vient en premier à l'esprit quand on pense aux dragons. Ils
            possèdent quatre pattes, de grandes ailes, une longue queue et parviennent à cracher du feu. Le dragon
            occidental est un terme général sous lequel on peut regrouper les dragons européens.
            <br>
            Dans les illustrations du Moyen Âge, les dragons médiévaux étaient représentés comme étant semblables à de
            grands lézards avec des ailes bien trop petites pour leur permettre de voler ; cependant, l'anatomie du
            dragon a été influencée avec l'évolution des connaissances sur les dinosaures.
            <br>
            Après que les gens se soient rendu compte que les dinosaures étaient plus proches des oiseaux et des
            mammifères que des lézards, l'imagerie des dragons a changé. C'est ainsi qu'ils ont commencé à être
            représentés avec de grandes ailes leur permettait de voler dans le ciel.
            <br>
            Alors que les dragons orientaux agissent comme des gardiens et symbolisent la prospérité et le pouvoir, les
            dragons occidentaux eux symbolisent la destruction, la mort et étaient souvent associés au Diable.
        </p>
        <li id="wyvern" class="photos drag3">La Wyvern:</li>
        <p>
            Originaire d'angleterre, la wyverne se distingue du dragon occidental standard par le fait qu'il est bipède,
            c'est-à-dire qu'il n'a que deux pattes au lieu de quatre. Ils sont généralement plus petits que les autres
            dragons et possèdent parfois des dards venimeux au bout de leur queue. À l'inverse du dragon médiéval, la
            wyvern ne crache pas de feu.
            <br>
            Pour certains, la Wyvern représente la protection, la force et la vaillance, mais peut aussi être un signe
            de vengeance.
        </p>
        <li id="hydre" class="photos drag4">L'Hydre:</li>
        <p>
            Le trait le plus notoire de l'Hydre est le fait que lorsqu'une tête est coupée, au moins une autre pousse à
            sa
            place. Parfois, chaque tête de l'Hydre a une capacité différente, comme cracher du venin ou cracher du feu,
            et
            généralement le sang et les crocs d'une Hydre sont aussi très toxiques. Tout cela les rend extrêmement
            difficiles à tuer.
            <br>
            Selon la mythologie grecque, l'Hydre lernéenne était un dragon aquatique serpentin dont le repaire se
            trouvait
            dans le lac de Lerne en Argolide. Le nombre exact de têtes d'une Hydre varie selon les sources, mais dans la
            mythologie grecque, il est dit que l'Hydre a plus de têtes que les peintres de vases ne pourraient en
            peindre et
            l'une d'entre elles est immortelle.
            <br>
            L'Hydre de Lerne est vaincue par Héraclès dans le cadre de l'un de ses douze travaux. En découvrant que deux
            têtes repoussent lorsqu'il en coupe une, Héraclès appelle son neveu Iolaus à l'aide. Iolaus procède à la
            cautérisation des blessures de l'Hydre après qu'Héraclès a coupé les têtes du dragon, brûlant les moignons
            frais
            avant qu'ils ne puissent se régénérer. Enfin, Héraclès coupe la tête immortelle de l'Hydre et l'enterre sous
            un
            rocher.
        </p>
        <li id="japonais" class="photos drag5">Le dragon japonais:</li>
        <p>
            Les dragons japonais sont des amalgames de dragons de la mythologie chinoise, indienne et coréenne, combinés
            au
            folklore local. Comme de nombreux dragons d'Asie de l'Est, les dragons japonais sont presque toujours des
            dieux
            de l'eau associés à la pluie, aux tempêtes, aux océans et aux étendues d'eau. Ces dragons seront
            généralement
            représentés comme des créatures serpentines sans ailes, qui ont des pieds griffus et de longues moustaches.
            <br>
            Ils peuvent également avoir la capacité de prendre une forme humaine, soit un homme ou une femme. Les
            Japonais
            ont tendance à distinguer les dragons japonais et d'Asie de l'Est des dragons occidentaux par le langage
            utilisé
            pour s'y référer, en utilisant « doragon » pour parler des dragons occidentaux, par opposition à d'autres
            noms
            comme « ryū » ou « tatsu » pour les dragons asiatiques.
            <br>
            Il existe un grand nombre de dragons japonais, l'un des plus célèbres est Ryūjin, dieu de la mer et maître
            des
            serpents qui vit dans un palais sous la mer. Bien qu'il soit souvent considéré comme bon, il figure dans
            divers
            contes, aidant parfois les protagonistes, mais devenant aussi occasionnellement un obstacle.
        </p>
        <li id="drug" class="photos drag6">Le Druk:</li>
        <p>
            Le Druk, également connu sous le nom de dragon du tonnerre, est issu de la mythologie tibétaine et
            bhoutanaise
            et est un symbole national du Bhoutan. Il est tellement célèbre là-bas que les dirigeants bhoutanais étant
            appelés « Druk Gyalpo », ce qui se traduit par « rois dragons du tonnerre« . Il apparaît sur le drapeau avec
            quatre joyaux dans ses griffes pour représenter la richesse.
            <br>
            Les dragons tibétains ont un corps long et mince, il s'agirait d'une adaptation à la haute altitude dans
            laquelle ils vivent. Ils ont quatre pattes avec cinq orteils sur chaque pied et sont de couleur vive
            (souvent
            rouge et jaune) car ils n'ont pas besoin de se camoufler. Ils vivent sur les montagnes enneigées, se
            déplaçant
            plus haut au fur et à mesure que la neige fond, les dragons les plus anciens vivant aux sommets de
            l'Himalaya.
            <br>
            Les dragons tibétains sont très vertueux; ils sont sages et sont capables de discerner la vérité et les
            mensonges. On dit qu'ils rejoignent les moines en méditation mais, bien que les dragons soient tout à fait
            capables de communiquer par la parole, il leur est interdit de leur parler - du moins verbalement.
            <br>
            Les dragons tibétains sont des êtres solitaires et sont invisibles pour les humains ; cependant, ils feront
            connaître leur présence par un coup de foudre, ce qui a pour avantage de faire prendre conscience de leurs
            erreurs aux personnes qui empruntent le mauvais chemin et de les amener à commencer à vivre leur vie de
            façon
            vertueuse.
            <br>
            Une branche du bouddhisme tibétain a adopté le Druk comme emblème, après que son fondateur ait perçu un
            présage
            alors qu'il construisait le monastère de Ralung. Alors que ce dernier était en cours de construction, un
            orage
            éclatât, et comme le tonnerre était considéré comme le rugissement d'un dragon, le fondateur prit cela comme
            un
            signe et rebaptisa le monastère Drug Ralung, les disciples devenant connus sous le nom de Drugpa « ceux du
            tonnerre ».
        </p>
        <li id="africain" class="photos drag7">Le dragon africain:</li>
        <p>
            Les dragons africains ne sont pas aussi connus que leurs homologues occidentaux ou asiatiques. En fait, ces
            dragons ont un aspect assez différent de notre représentation traditionnelle des dragons. La plupart des
            dragons
            d'Afrique ont l'apparence de serpents géants et, selon l'histoire, ils ont parfois des jambes, parfois non.
            <br>
            Ces dragons ressemblant à des serpents sont apparus à de nombreuses reprises dans la culture africaine, que
            ce
            soit dans la mythologie, la religion ou les contes tribaux. On trouve des histoires sur ces créatures dans
            d'innombrables villes et villages d'Afrique.
        </p>
        <h3>La légende de Mokele-Mbembe</h3>
        <p>
            Une histoire connue en Afrique est celle de Mokele-Mbembe, un dragon qui serait en fait un dinosaure dont
            les
            ancêtres auraient survécu à l'astéroïde qui causa la fin des dinosaures. Cette légende naquît au 16e siècle
            ou
            des rumeurs de bêtes gigantesques semblables à des dragons vivaient cachés dans la région du Congo. Cette
            histoire captiva la communauté scientifique une partie du 20e siècle, malheureusement on se rendit compte
            qu'il
            ne s'agissait en fait que de fausses histoires, à l'image du monstre du Loch Ness. Ces histoires furent très
            probablement alimentées par les ossements de dinosaures qui abondaient dans la région.
        </p>
        <li id="gaas" class="photos drag8">le Gaasyendietha:</li>
        <p>
            Dans les premiers récits iroqouis et algonquins, les gens parlaient d'une race de dragons serpentins géants
            vivant dans le lac Ontario, les Sénécas étant convaincus de l'existence du Gaasyendietha, un serpent géant
            avec
            une tête d'hydre et la capacité de voler et de cracher du feu. On disait qu'il vivait principalement dans le
            lac
            Ontario, mais qu'il pouvait se déplacer vers d'autres plans d'eau du Canada.
            <br>
            Il existe deux légendes traditionnelles orales concernant la naissance du Gaasyendietha ; la première dit
            qu'il
            est issu d'œufs de serpent, tandis que l'autre prétend qu'il est arrivé sur terre dans un météore (ce qui en
            fait techniquement un extraterrestre). En tant que tel, le Gaasyendietha est également connu sous le nom de
            «
            dragon météorique« , et il est dit qu'il vole dans le ciel sur un chemin fait de feu.
        </p>
        <li id="quetza" class="photos drag9">Le Quetzalcóatl:</li>
        <p>
            Avec un nom signifiant « serpent à plumes », le Quetzalcóatl est un dragon dont la physiologie combine les
            caractéristiques des oiseaux et des serpents à sonnettes. Il est l'un des dieux les plus vénérés de
            l'ancienne
            Mésoamérique. Il a des liens avec les dieux de la pluie, du vent et de l'aube, et était le donneur de maïs
            aux
            Aztèques, ce qui était très important pour une société qui dépendait d'un secteur agricole florissant pour
            survivre.
            <br>
            Il agissait également en tant que protecteur des artisans et était le Patron de la connaissance, de la
            science
            et des arts, les Aztèques croyant qu'il avait inventé les livres et les calendriers. Le Quetzalcóatl était
            même
            responsable de l'organisation du cosmos originel et a contribué à la création des humains. Il était si
            respecté
            que les grands prêtres aztèques incluaient son nom dans leur titre pour souligner leur rang.
        </p>
        <li id="vishap" class="photos drag10">Le Vishap:</li>
        <p>
            Serpent ailé issu de la mythologie arménienne, le Vishap est un dragon fortement associé à l'eau et se
            compose de parties du corps d'autres animaux. Lorsqu'ils montent aux cieux ou descendent sur terre, ils
            provoquent des éclipses et/ou des orages.
            <br>
            Il représente l'eau, la fertilité, la richesse et un pouvoir incroyable, mais il a une vilaine manie : il
            est connu pour voler les enfants humains et les remplacer par sa propre progéniture. Il y a aussi des
            histoires de Vishaps forçant les gens à leur sacrifier des femmes, mais ces dernières seraient finalement
            sauvées par des héros, chasseurs de dragons.
            <br>
            Selon la légende, les Vishaps vivent dans les cieux et les nuages, les hautes montagnes et les grands lacs,
            et les Vishaps qui atteignent plus de mille ans ont le potentiel dengloutir le monde.
        </p>
        <p>
            On est bien loin de l'image de l'énorme monstre reptilien crachant du feu, véhiculée par la plupart des
            œuvres de fantasy.
            Le simple échantillon de dragons inclus dans cette liste montre que de nombreux dragons mythologiques ont en
            fait une plus grande affinité pour l'eau que pour le feu et sont plus étroitement liés aux serpents qu'aux
            lézards ou aux dinosaures.
