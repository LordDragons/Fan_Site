        <p>
            La Bête de l'Apocalypse, (τὸ Θηρίον (tò thēríon) en grec ancien) est une figure de l'eschatologie chrétienne
            qui apparait dans le chapitre 13 de l'Apocalypse de Jean, en écho à la Vision des quatre bêtes du Livre de
            Daniel.
            L'auteur de l'Apocalypse, qui écrit sous le règne de l'empereur Domitien, décrit successivement deux bêtes
            affidées à Satan — l'une issue de la mer, l'autre issue de la terre pour seconder la première — qui
            symbolisent l'oppression du pouvoir romain idolâtre et les moyens qu'il déploie à cette fin.
