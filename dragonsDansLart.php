    <h2>Le dragon dans l'art et la culture populaire:</h2>
    <p>Le dragon a été repris dans la littérature moderne et le cinéma, ainsi que dans les jeux de rôle (essentiellement
        ceux d'inspiration médiévale-fantastique).</p>

    <h3>Littérature:</h3>
    <p>La littérature de fantasy donne souvent une part importante aux dragons. Le plus souvent l'image qui en ressort
        est en adéquation avec celle donnée par les mythes et le folklore européen.
        En ce sens les histoires modernes le représentent comme extrêmement intelligent, doué de parole et parfois
        associé à la magie. Le sang du dragon possède également des propriétés magiques comme dans l'opéra Siegfried,
        celui-ci devient capable de comprendre les oiseaux de la forêt.
        Il est généralement le gardien d'un trésor caché dans une citadelle ou une grotte (voire d'une princesse dans
        les contes).
    </p>
    <div id="hsf" class="dragon-litterature">
        <img src="images/histoire-sans-fin.jpg">
    </div>
    <p>Cependant avec l'engouement du public pour les mangas et la culture asiatique, les dragons orientaux ont tendance
        à entrer dans l'imaginaire collectif occidental. Par exemple le roman de l'écrivain allemand Michael Ende,
        L'Histoire sans fin, adapté en film par la suite, étonna les lecteurs occidentaux en montrant un sage dragon
        dénué d'ailes et possédant une gueule de lion dans le livre, et de chien dans le film.
    </p>
    <h4 id="smaug">Univers de la Terre du Milieu:</h4>
    <p>
        Dans l'univers de la Terre du Milieu de l'écrivain J. R. R. Tolkien, les dragons sont des créatures maléfiques
        nées des œuvres de Morgoth au cours du Premier Âge en Angband. Le premier d'entre eux, surnommé le « Père des
        Dragons » s'appelle Glaurung. Ils sont parfois appelés vers, car les premiers dragons étaient aptères (sans
        ailes) et se déplaçaient en rampant (par exemple, Glaurung laisse une traînée de cendres derrière lui) ; le mot
        est resté pour décrire les dragons, même ailés - lesquels ne sont apparus que lors de la guerre de la Grande
        Colère, à la fin du Premier Âge.</p>
    <div class="dragon-litterature">
        <img src="images/smaug.jpg">
    </div>
    <p>L'antagoniste principal du roman Le Hobbit est le dragon Smaug, qui a spolié les nains de leur royaume sous la
        Montagne Solitaire et a volé leur trésor.
        <br>
        Les dragons de Tolkien allient une avarice prodigieuse à une grande ruse. Ils aiment à amasser des richesses
        pour s'en faire un lit et dormir dessus, tel Glaurung à Nargothrond ou Smaug en Erebor.
    </p>
    <h3>Cinéma:</h3>
    <p>Le cinéma (et par extension tout support audiovisuel) est un vecteur important de l'imaginaire collectif moderne,
        et les dragons n'y font pas exception. Ils y apparaissent fréquemment, a fortiori depuis l'avènement des effets
        spéciaux élaborés et l'engouement du public pour les films fantastiques à grand budget.</p>
    <div id="cinema" class="dragon-cinema-titre">
        <li>-Les Nibelungen (1924) de Fritz Lang :</li> Siegfried terrasse le dragon et devient invincible en se
        baignant dans son sang.
        <li>-Dragon du lac de feu (1981) :</li> l'abominable pacte conclu entre un roi et un dragon prêt à tout pour
        conserver son trône en échange de jeunes filles vierges.
        <li>-Excalibur (1981) :</li> dans cette adaptation de la légende arthurienne, Merlin est le détenteur de
        pouvoirs fabuleux tous issus du Dragon. Merlin utilise par exemple le souffle du Dragon pour permettre à Uther
        Pendragon de prendre la place du duc de Cornouailles.
        <li>-L'histoire sans fin (1984) :</li> Bastien mène une quête avec l'aide d'un dragon à travers Fantasia.
        <li>-Cœur de dragon (1996) :</li> au Moyen Âge, l'amitié improbable entre un chevalier de l'ancien code et le
        dernier des dragons.
        <li>-Donjons et Dragons (2000) et Donjons et Dragons, la puissance suprême (2006) :</li> inspirés du jeu de
        rôle.
        <li>-Le Règne du feu (2003) :</li> au début du xxie siècle, des travaux dans Londres réveillent les dragons qui
        envahissent la terre et détruisent toute la civilisation humaine. Seules quelques poignées d'hommes résistent.
        <li>-Les chroniques du dragon (2008,TV) :</li> l'histoire du paisible royaume de Carpia ravagé par les exactions
        d'un dragon de feu.
        <li>-Dragons, Dragons 2 et Dragons 3 (How To Train Your Dragon, 2010)-(How To Train Your Dragon 2, 2014)-(How To
            Train Your Dragon 3: The Hidden World, 2019) :</li> films d'animation en 3D et 2D des studios Dreamworks
        mettant en scène l'amitié entre un Viking et un Dragon alors que les deux peuples sont ennemis.
        <li>-Moi, Arthur, 12 ans, chasseur de dragons24 (2010):</li> film américain où le sujet principal est le dragon
        Dark Smoke, seigneur maléfique d'un jeu nommé Elixir Quest.
