    <h3>Jeux de rôle ,jeux vidéo et jeux de cartes</h3>
    <p>De par la puissance qu'ils inspirent, les dragons sont une figure récurrente dans l'univers ludique, qu'il se
        déroule sur papier ou par l'intermédiaire d'un support audiovisuel. Bien que dans plusieurs jeux ils n'occupent
        qu'une place secondaire au sein d'un bestiaire, la majeure partie du temps ce sont des créatures
        impressionnantes faisant partie intégrante du scénario et du toile de fond du jeu. La présence de dragons dans
        les jeux vidéo se remarque d'autant plus que ceux-ci possèdent souvent un nom éponyme et aussi dans des jeux de
        cartes. </p>
    <h4 id="D&D">Donjons et Dragons</h4>
    <p>
        Dans Donjons et Dragons, le dragon est l'une des figures les plus emblématiques de ce jeu de rôle, à la fois de
        par la puissance qu'il dégage et du développement de son histoire (un ouvrage entier, le Draconomicon, est
        consacré au dragon). Puissance parfois quasi-divine, les dragons participent souvent aux grands changements des
        différents mondes constituant le multivers de Donjons et Dragons (ci-dessous : Tiamat Reine mére des dragons). Il existe trois types de dragons d'alignements
        différents, chromatiques (mauvais) et métalliques (bons) et les dragons de gemmes (neutres). Des dragons plus
        particuliers existent par ailleurs.
    </p>
