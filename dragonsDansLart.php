    <h4 id="hunter">Monster Hunter</h4>
    <p>
        Dans Monster Hunter, des monstres fantastiques sont les principaux adversaires des chasseurs. Toutefois, ces
        monstres sont en grande majorité des wyvernes et non pas des dragons (pas de pattes antérieures) : Rathalos,
        Rathian, Diablos et Astalos sont quelques exemples. On trouve également quelques léviathans : des reptiles
        similaires aux dragons (pattes antérieures et postérieures) mais sans ailes et avec un corps allongé. L'Agnaktor
        et le Lagiacrus constituent deux exemples de cette catégorie. Enfin, il existe tout de même quelques vrais
        dragons dans la série, mais il faut les chercher du côté des monstres les plus puissants, dits "dragons anciens"
        (cette appellation ne regroupe pas que des dragons) : Kushala Daora, Teostra et Chameleos sont trois exemples.
    </p>
